$(document).ready(function () {
    $(".content").show();
    $(".showbookdiv").show();
    $(".artistdiv").hide();
    $(".bookdetail").hide();
    $('.artisttable').hide();
    $('.execuliveshow').hide();
    $(".gstarea").hide();
    $('.showadd').hide();
    $('.bookingarea').hide();
    $(".showbookdetail").hide();
    $(".artist").click(function () {
        $(".content").hide();
        $('.bookingarea').hide();
        $(".bookdetail").hide();
        $('.showbookdiv').hide();
        $(".showbookdetail").hide();
        $('.execuliveshow').hide();
        $(".gstarea").hide();
        $('.showadd').hide();
        $('.artisttable').hide();
        $(".showbookdiv").hide();
        $(".artistdiv").show();
    });

});

